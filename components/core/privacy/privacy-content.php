<h3>A cookie-k használatával kapcsolatos tájékoztatás</h3>
<h4>Mi az a cookie?</h4>
<p>Az Adatkezelő a honlap látogatása során úgynevezett cookie-kat (sütiket) használ. A cookie betűből és számokból álló információcsomag, amit honlapunk az Ön böngészőjének küld el azzal a céllal, hogy elmentse bizonyos beállításait, megkönnyítse a honlapunk használatát és közreműködik abban, hogy néhány releváns, statisztikai jellegű információt gyűjtsünk a látogatóinkról.</p>
<p>A cookie-k (sütik) egy része nem tartalmaz személyes információkat, és nem alkalmas az egyéni felhasználó azonosítására, egy részük azonban olyan egyéni azonosítót tartalmaz - egy titkos, véletlenül generált számsort - amelyet az Ön eszköze tárol, ezzel az Ön azonosíthatóságát is biztosítja. Az egyes cookie-k (sütik) működési időtartamát az egyes cookie-k (sütik) vonatkozó leírása tartalmazza.</p>
<h4>A cookie-k jogszabályi háttere és jogalapja:</h4>
<p>Az adatkezelés jogalapja a Rendelet 6. cikk (1) bekezdés a) pontja alapján az Ön hozzájárulása.</p>

<h4>A honlap által használt cookie-k főbb jellemzői:</h4>
<h5>Google Analytics cookie: </h5>
<p>A Google Analytics a Google elemző eszköze, amely abban segít a weboldalak és alkalmazások tulajdonosainak, hogy pontosabb képet kapjanak látogatóik tevékenységeiről. A szolgáltatás cookie-kat használhat, hogy információkat gyűjtsön és jelentést készítsen a weboldal használatára vonatkozó statisztikai adatokból anélkül, hogy egyénileg azonosítaná a látogatókat a Google számára. A Google Analytics által használt fő cookie a „__ga” cookie. A webhelyhasználati statisztikai adatokból készülő jelentések mellett a Google Analytics – az előbbiekben ismertetett néhány hirdetési cookie-val együtt – felhasználható arra is, hogy relevánsabb hirdetéseket jelenítsünk meg a Google-termékekben (például a Google Keresésben) és szerte az interneten.</p>

<h5>A működéshez szigorúan szükséges cookie-k: </h5>
<p>Ezek a cookie-k a weboldal használatához nélkülözhetetlenek, és lehetővé teszik a weboldal alapvető funkcióinak használatát. Ezek hiányába az oldal számos funkciója nem lesz elérhető az Ön számára. Ezen típusú cookie-k élettartama kizárólag a munkamenet idejére korlátozódik.</p>

<h5>A felhasználói élmény javítását szolgáló cookie-k: </h5>
<p>Ezek a cookie-k információkat gyűjtenek a felhasználó weboldalhasználatáról, például, hogy mely oldalakat látogatja leggyakrabban, vagy milyen hibaüzenetet kap a weboldalról. Ezek a cookie-k nem gyűjtenek a látogatót azonosító információkat, vagyis teljesen általános, névtelen információkkal dolgoznak. Az ezekből nyert adatokat a weboldal teljesítményének javítására használjuk. Ezen típusú cookie-k élettartama kizárólag a munkamenet idejére korlátozódik.</p>

<h5>Munkamenet cookie: </h5>
<p>Ezek a sütik a látogató tartózkodási helyét, a böngésző nyelvét, a fizetés pénznemét tárolják, élettartamuk a böngésző bezárása, vagy maximum 2 óra.</p>

<h5>Mobil verzió, design cookie:</h5>
<p>Érzékeli a látogató használt eszközt, és mobilon átvált teljes nézetre. Élettartama 365 nap.</p>

<h5>Cookie elfogadás cookie:</h5>
<p>Az oldalra érkezéskor a figyelmeztető ablakban elfogadja a cookiek tárolásáról szóló nyilatkozatot. Élettartama 365 nap.</p>

